package Encapsulamento;

public class Pessoa {

	private String Nome ;
	
	private int Idade ;
	
	public void setNome (String Nome)
	{
		this.Nome = Nome ;
	}
	
	public String getNome()
	{
		return Nome ;
	}
	
	public void setIdade (int Idade)
	{
		this.Idade = Idade ;
	}
	
	public int getIdade ()
	{
		return Idade ;
	}
	
}//João Pedro Vieira Luz Piva